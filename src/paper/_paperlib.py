def repo_root() -> Path:
    """Walk up until we find the repo (the dir holding tex/paper.tex)."""
    d = Path(__file__).resolve()
    for p in [d, *d.parents]:
        if (p / "tex" / "paper.tex").exists():
            return p
    raise SystemExit("cannot locate repo root (tex/paper.tex not found)")


