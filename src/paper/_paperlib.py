def run(cmd, cwd=None):
    return subprocess.run(cmd, cwd=cwd or REPO, capture_output=True, text=True)


def cite_keys() -> set:
    txt = PAPER.read_text()
    keys = set()
    for m in re.finditer(r"\\cite[a-z]*\{([^}]+)\}", txt):
        for k in m.group(1).split(","):
            keys.add(k.strip())
    return keys


def bib_keys() -> set:
    return set(re.findall(r"^@[a-zA-Z]+\{([^,]+),", BIB.read_text(), re.M))


def record_keys() -> set:
    return {p.stem for p in RECORDS.glob("*.md")}


def ledger_rows(kind: str) -> list:
    """Return [(clm, bibkey-or-binding, full-binding)] for CITE or RESULT rows."""
    rows = []
    for ln in LEDGER.read_text().splitlines():
        if ln.startswith("#") or not ln.strip():
            continue
        f = ln.split("\t")
        if len(f) >= 4 and f[1] == kind:
            rows.append((f[0], f[3].split()[0], f[3]))
    return rows


def reflex_macro(name: str):
    m = re.search(r"\\newcommand\{\\" + name + r"\}\{([^}]*)\}", REFLEX.read_text())
    return m.group(1) if m else None


class Gate:
    """Tiny pass/fail reporter shared by the layer scripts (mirrors src/A..D)."""

    def __init__(self):
        self.ok = True

    def check(self, label, cond, detail=""):
        tag = "[PASS]" if cond else "[FAIL]"
        if not cond:
            self.ok = False
        print(f"{tag} {label}" + (f" -- {detail}" if detail else ""))
        return cond

    def skip(self, label, detail=""):
        print(f"[SKIP] {label}" + (f" -- {detail}" if detail else ""))
