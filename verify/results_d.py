#!/usr/bin/env python3
"""Use Case D (deductive / Rocq) paper macros -> tex/macros/results_d.tex.

Reads the committed squeeze result src/D/evidence/results.json (produced by the
real Rocq run) and emits the macros the manuscript cites for the fourth executable
instance, so the numbers recompute. D is the second instance on the TRANSCRIPTION
terrain (English textbook math -> formal Rocq statement), alongside A.
"""
import json
import sys
from pathlib import Path

REPO = Path(__file__).resolve().parents[1]
SRC = REPO / "src" / "D" / "evidence" / "results.json"
MACROS = REPO / "tex" / "macros" / "results_d.tex"


def main():
    R = json.loads(SRC.read_text())
    seeded = R["seeded_mutations"]
    caught_on = R["defects_caught"]
    caught_off = R["ablation_barrier_off_catchable"]
    if caught_on is None or caught_off is None:
        print("D results.json has null kernel verdicts (Rocq was absent at measure "
              "time); re-run src/D with Rocq before regenerating.", file=sys.stderr)
        return 1
    pct = "100\\%" if caught_on == seeded else f"{round(100*caught_on/seeded)}\\%"
    macro = {
        "ResDSeededDefects": seeded,
        "ResDDefectsCaught": caught_on,
        "ResDBarrierOff": caught_off,
        "ResDClauses": R["manifest_clauses"],
        "ResDDetectionPct": pct,
    }
    lines = ["% GENERATED by verify/results_d.py -- do not hand-edit."]
    for k in sorted(macro):
        lines.append(f"\\newcommand{{\\{k}}}{{{macro[k]}}}")
    MACROS.write_text("\n".join(lines) + "\n")
    print(json.dumps(macro, indent=2, sort_keys=True))
    print("macros ->", MACROS)
    return 0


if __name__ == "__main__":
    sys.exit(main())
