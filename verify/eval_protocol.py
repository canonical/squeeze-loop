#!/usr/bin/env python3
"""Runnable harness for the Proposed Evaluation Protocol (paper §sec:eval).

This operationalizes the protocol into a config-matrix experiment driver. It
RUNS the parts that are measurable now on the three deterministic executable
instances, and STUBS the parts that need real LLM-agent runs --- reporting each
stub's prerequisites rather than fabricating a result (fabricating one would be
the coherent-and-wrong failure the paper is about).

Outputs:
  - eval/results.json        machine-readable protocol-status manifest
  - tex/macros/eval.tex      generated counts for §sec:eval (\\ResEval...)

Wire-in point: replace the `runner` of each PENDING config with a real
LLM-agent pipeline + task suite, and its status flips to MEASURED.
"""

import json
import re
import sys
from pathlib import Path

REPO = Path(__file__).resolve().parents[1]
INSTANCES = ["A", "B", "C"]
OUT_JSON = REPO / "eval" / "results.json"
OUT_TEX = REPO / "tex" / "macros" / "eval.tex"


def load_detection():
    seeded = on = off = 0
    for t in INSTANCES:
        R = json.loads((REPO / "src" / t / "evidence" / "results.json").read_text())
        seeded += R["seeded_defects"]
        on += R["ablation_barrier_on_caught"]
        off += R["ablation_barrier_off_caught"]
    return seeded, on, off


def load_coupling():
    tex = (REPO / "tex" / "macros" / "coupling.tex").read_text()
    out = {}
    for t in INSTANCES:
        m = re.search(r"\\ResCoupling" + t + r"val\}\{([0-9.]+)\}", tex)
        out[t] = float(m.group(1)) if m else None
    return out


# --- the config matrix (squeeze vs baselines) -------------------------------
# runner=None marks a PENDING config that needs a real LLM-agent pipeline.
CONFIGS = [
    {"id": "squeeze", "desc": "squeeze-compliant (physical barrier on)", "runnable": True},
    {"id": "barrier_off", "desc": "barrier off: exerciser anchored to the implementation", "runnable": True},
    {"id": "shared_context", "desc": "role-differentiated, shared-transcript multi-agent baseline",
     "runnable": False, "needs": ["LLM agent pipeline", "matched-difficulty task suite"]},
    {"id": "self_refine", "desc": "single-agent self-refine baseline",
     "runnable": False, "needs": ["LLM agent pipeline", "task suite"]},
    {"id": "debate", "desc": "multi-agent debate baseline",
     "runnable": False, "needs": ["LLM agent pipeline", "task suite"]},
]

HYPOTHESES = [
    {"id": "H1", "desc": "disjointness lowers the coherent-and-wrong rate vs shared context",
     "runnable": False, "needs": ["squeeze + baseline runs at matched difficulty", "repetitions + significance test"]},
    {"id": "H2", "desc": "physical barriers reduce evidence-implementation coupling and catch more seeded defects",
     "runnable": True},
    {"id": "H3", "desc": "Gate-A amendment rate predicts the downstream gap rate",
     "runnable": False, "needs": ["a corpus with logged Gate-A amendments and per-item gap rates"]},
    {"id": "H4", "desc": "protocol overhead amortizes on long regression horizons",
     "runnable": False, "needs": ["cost/latency telemetry across engagements of varying length"]},
]


def main():
    seeded, on, off = load_detection()
    coupling = load_coupling()

    # H2 is the runnable hypothesis: barrier on/off detection + coupling, on the
    # three instances. Everything quantitative here is read from the instances.
    h2 = {
        "seeded_total": seeded,
        "detection_on_pct": round(100 * on / seeded),
        "detection_off_pct": round(100 * off / seeded),
        "coupling_pct": coupling,
        "n_instances": len(INSTANCES),
    }

    configs_runnable = [c["id"] for c in CONFIGS if c["runnable"]]
    configs_pending = [c["id"] for c in CONFIGS if not c["runnable"]]
    hyp_measured = [h["id"] for h in HYPOTHESES if h["runnable"]]
    hyp_pending = [h["id"] for h in HYPOTHESES if not h["runnable"]]

    manifest = {
        "configs": CONFIGS,
        "hypotheses": HYPOTHESES,
        "H2_measured": h2,
        "summary": {
            "configs_runnable": len(configs_runnable),
            "configs_pending": len(configs_pending),
            "hypotheses_measured": len(hyp_measured),
            "hypotheses_pending": len(hyp_pending),
        },
    }
    OUT_JSON.parent.mkdir(parents=True, exist_ok=True)
    OUT_JSON.write_text(json.dumps(manifest, indent=2, sort_keys=True) + "\n")

    macro = {
        "ResEvalHypTotal": len(HYPOTHESES),
        "ResEvalHypMeasured": len(hyp_measured),
        "ResEvalHypPending": len(hyp_pending),
        "ResEvalConfigsRunnable": len(configs_runnable),
        "ResEvalConfigsPending": len(configs_pending),
    }
    OUT_TEX.parent.mkdir(parents=True, exist_ok=True)
    lines = ["% GENERATED by verify/eval_protocol.py -- do not hand-edit."]
    for k in sorted(macro):
        lines.append(f"\\newcommand{{\\{k}}}{{{macro[k]}}}")
    OUT_TEX.write_text("\n".join(lines) + "\n")

    # --- report -------------------------------------------------------------
    print("=== Evaluation Protocol status ===")
    print("Configs:")
    for c in CONFIGS:
        tag = "RUN " if c["runnable"] else "PEND"
        extra = "" if c["runnable"] else "  needs: " + "; ".join(c["needs"])
        print(f"  [{tag}] {c['id']:15s} {c['desc']}{extra}")
    print("Hypotheses:")
    for h in HYPOTHESES:
        tag = "MEASURED" if h["runnable"] else "PENDING "
        extra = "" if h["runnable"] else "  needs: " + "; ".join(h["needs"])
        print(f"  [{tag}] {h['id']}: {h['desc']}{extra}")
    print(f"\nH2 (measured on {h2['n_instances']} instances): "
          f"detection barrier-on {h2['detection_on_pct']}% / off {h2['detection_off_pct']}% "
          f"over {seeded} seeded defects; coupling {coupling}")
    print(f"\nrunnable configs: {len(configs_runnable)}/{len(CONFIGS)}; "
          f"measured hypotheses: {len(hyp_measured)}/{len(HYPOTHESES)}")
    print("macros ->", OUT_TEX)
    return 0


if __name__ == "__main__":
    sys.exit(main())
