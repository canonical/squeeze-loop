#!/usr/bin/env python3
"""Score pilot-3: weak self-author vs independent exercisers, impl held fixed.

cases/<task>.selfauthor.json    {"impl": "<source>", "tests": [{input,expected}]}
cases/<task>.indep_weak.json     [{input, expected}]   (same weak model, spec only)
cases/<task>.indep_strong.json   [{input, expected}]   (strong model, spec only)

Among the self-authored impls that are actually buggy (vs the oracle on the
task's probes + all test inputs), report how many each condition catches (a
spec-correct test the impl fails). NOTE: execs agent-generated code (tiny pure
functions); flagged.
"""

import json
import sys
from pathlib import Path

HERE = Path(__file__).resolve().parent
REPO = HERE.parents[1]
CASES = HERE / "cases"
CONDS = ["self", "indep_weak", "indep_strong"]

sys.path.insert(0, str(HERE))
import tasks  # noqa: E402


def _eq(a, b):
    if isinstance(a, bool) or isinstance(b, bool):
        return a == b
    if isinstance(a, (int, float)) and isinstance(b, (int, float)):
        return abs(a - b) < 1e-9
    return a == b


def main():
    counts = {c: 0 for c in CONDS}
    n_buggy = 0
    per = {}
    for t in tasks.TASKS:
        name, oracle, probes = t["name"], t["correct"], t["probes"]
        sa = CASES / f"{name}.selfauthor.json"
        if not sa.exists():
            per[name] = {"note": "no selfauthor file"}
            continue
        blob = json.loads(sa.read_text())
        try:
            ns = {}
            exec(blob["impl"], ns)            # noqa: S102 (agent code, flagged)
            f = ns[name]
        except Exception as e:
            per[name] = {"note": f"impl load failed: {e}"}
            continue
        self_tests = blob.get("tests", [])
        testsets = {
            "self": self_tests,
            "indep_weak": json.loads((CASES / f"{name}.indep_weak.json").read_text())
            if (CASES / f"{name}.indep_weak.json").exists() else [],
            "indep_strong": json.loads((CASES / f"{name}.indep_strong.json").read_text())
            if (CASES / f"{name}.indep_strong.json").exists() else [],
        }

        inputs = list(probes) + [c["input"] for ts in testsets.values() for c in ts]
        buggy = False
        for inp in inputs:
            try:
                if not _eq(f(inp), oracle(inp)):
                    buggy = True
                    break
            except Exception:
                buggy = True
                break

        def caught(ts):
            for c in ts:
                inp, exp = c.get("input"), c.get("expected")
                try:
                    o, fi = oracle(inp), f(inp)
                except Exception:
                    continue
                if _eq(exp, o) and not _eq(o, fi):
                    return True
            return False

        rec = {"buggy": buggy}
        for c in CONDS:
            rec[c] = caught(testsets[c])
        if buggy:
            n_buggy += 1
            for c in CONDS:
                counts[c] += rec[c]
        per[name] = rec

    result = {"per_task": per, "summary": {"tasks": len(tasks.TASKS),
              "buggy_impls": n_buggy, "caught_of_buggy": counts}}
    (HERE / "results.json").write_text(json.dumps(result, indent=2, sort_keys=True) + "\n")

    macro = {
        "ResPilotThreeTasks": len(tasks.TASKS),
        "ResPilotThreeBuggy": n_buggy,
        "ResPilotThreeSelf": counts["self"],
        "ResPilotThreeIndepWeak": counts["indep_weak"],
        "ResPilotThreeIndepStrong": counts["indep_strong"],
    }
    mac = REPO / "tex" / "macros" / "pilot3.tex"
    mac.parent.mkdir(parents=True, exist_ok=True)
    lines = ["% GENERATED by eval/pilot3/score.py -- do not hand-edit."]
    for k in sorted(macro):
        lines.append(f"\\newcommand{{\\{k}}}{{{macro[k]}}}")
    mac.write_text("\n".join(lines) + "\n")

    print(json.dumps(result, indent=2, sort_keys=True))
    print(f"\nbuggy weak self-impls: {n_buggy}/{len(tasks.TASKS)}; "
          f"of those caught -- self {counts['self']}, indep_weak {counts['indep_weak']}, "
          f"indep_strong {counts['indep_strong']}")
    print("macros ->", mac)
    return 0


if __name__ == "__main__":
    sys.exit(main())
