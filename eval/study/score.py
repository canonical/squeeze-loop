#!/usr/bin/env python3
"""Score the powered study: self-preference / barrier effect across model tiers.

cases/<task>.self.<model>.json   {"impl": "<source>", "tests": [{input,expected}]}
cases/<task>.independent.json     [{input, expected}]   (independent opus, spec only)

For each (task, model) self-authored impl: decide if it is buggy (vs the oracle on
probes + all test inputs), and if so whether (a) its OWN tests catch it and (b)
the task's INDEPENDENT exerciser tests catch it. Aggregate over all buggy impls;
report a paired McNemar exact test on the discordant pairs (self-only vs
independent-only catches). NOTE: execs agent-generated code (tiny pure functions).
"""

import json
import math
import sys
from pathlib import Path

HERE = Path(__file__).resolve().parent
REPO = HERE.parents[1]
CASES = HERE / "cases"
MODELS = ["haiku", "sonnet", "opus"]

sys.path.insert(0, str(HERE))
import tasks  # noqa: E402


def _eq(a, b):
    if isinstance(a, bool) or isinstance(b, bool):
        return a == b
    if isinstance(a, (int, float)) and isinstance(b, (int, float)):
        return abs(a - b) < 1e-9
    return a == b


def mcnemar_exact_p(b, c):
    """Two-sided exact McNemar (binomial) p for discordant counts b, c."""
    n = b + c
    if n == 0:
        return 1.0
    k = min(b, c)
    tail = sum(math.comb(n, i) for i in range(k + 1)) * (0.5 ** n)
    return min(1.0, 2 * tail)


def main():
    per = {}
    bug_by_model = {m: 0 for m in MODELS}
    impls_by_model = {m: 0 for m in MODELS}
    buggy = []          # list of (self_caught, indep_caught)
    for t in tasks.TASKS:
        name, oracle, probes = t["name"], t["correct"], t["probes"]
        ind_f = CASES / f"{name}.independent.json"
        indep = json.loads(ind_f.read_text()) if ind_f.exists() else []
        for model in MODELS:
            f_path = CASES / f"{name}.self.{model}.json"
            if not f_path.exists():
                per[f"{name}.{model}"] = {"note": "missing"}
                continue
            blob = json.loads(f_path.read_text())
            try:
                ns = {}
                exec(blob["impl"], ns)        # noqa: S102 (agent code, flagged)
                f = ns[name]
            except Exception as e:
                per[f"{name}.{model}"] = {"note": f"impl load failed: {e}"}
                continue
            impls_by_model[model] += 1
            self_tests = blob.get("tests", [])
            inputs = list(probes) + [c["input"] for c in self_tests + indep]
            is_buggy = False
            for inp in inputs:
                try:
                    if not _eq(f(inp), oracle(inp)):
                        is_buggy = True
                        break
                except Exception:
                    is_buggy = True
                    break

            def caught(ts):
                for c in ts:
                    try:
                        o, fi = oracle(c["input"]), f(c["input"])
                    except Exception:
                        continue
                    if _eq(c.get("expected"), o) and not _eq(o, fi):
                        return True
                return False

            rec = {"buggy": is_buggy}
            if is_buggy:
                bug_by_model[model] += 1
                sc, ic = caught(self_tests), caught(indep)
                rec["self_caught"], rec["indep_caught"] = sc, ic
                buggy.append((sc, ic))
            per[f"{name}.{model}"] = rec

    self_caught = sum(1 for sc, _ in buggy if sc)
    indep_caught = sum(1 for _, ic in buggy if ic)
    b = sum(1 for sc, ic in buggy if sc and not ic)     # self only
    c = sum(1 for sc, ic in buggy if ic and not sc)     # independent only
    p = mcnemar_exact_p(b, c)

    result = {
        "per_impl": per,
        "impls_by_model": impls_by_model,
        "bug_by_model": bug_by_model,
        "n_impls": sum(impls_by_model.values()),
        "n_buggy": len(buggy),
        "self_caught_of_buggy": self_caught,
        "indep_caught_of_buggy": indep_caught,
        "discordant_self_only": b,
        "discordant_indep_only": c,
        "mcnemar_p": round(p, 4),
    }
    (HERE / "results.json").write_text(json.dumps(result, indent=2, sort_keys=True) + "\n")

    macro = {
        "ResStudyImpls": result["n_impls"],
        "ResStudyBuggy": result["n_buggy"],
        "ResStudySelfCaught": self_caught,
        "ResStudyIndepCaught": indep_caught,
        "ResStudyDiscSelf": b,
        "ResStudyDiscIndep": c,
        "ResStudyP": f"{p:.3f}",
    }
    mac = REPO / "tex" / "macros" / "study.tex"
    mac.parent.mkdir(parents=True, exist_ok=True)
    lines = ["% GENERATED by eval/study/score.py -- do not hand-edit."]
    for k in sorted(macro):
        lines.append(f"\\newcommand{{\\{k}}}{{{macro[k]}}}")
    mac.write_text("\n".join(lines) + "\n")

    print(json.dumps(result, indent=2, sort_keys=True))
    print(f"\nimpls {result['n_impls']}, buggy {result['n_buggy']} "
          f"(by model {bug_by_model}); of buggy: self caught {self_caught}, "
          f"independent caught {indep_caught}; discordant self-only={b} indep-only={c}; "
          f"McNemar p={p:.3f}")
    print("macros ->", mac)
    return 0


if __name__ == "__main__":
    sys.exit(main())
