#!/usr/bin/env python3
"""Score pilot-2: does an agent's own test suite catch its own bug, and does an
independent spec-only exerciser catch it, holding the self-authored implementation
fixed?

Inputs per task:
  cases/<task>.selfauthor.json   {"impl": "<python source>", "tests": [{input,expected}]}
  cases/<task>.independent.json   [{input, expected}]   (spec only)

For each task we exec the self-authored impl, decide whether it is buggy against
the oracle on the task's PROBES (plus all test inputs), and then ask whether each
config has a spec-correct test the impl actually fails. The comparison is only
meaningful on tasks whose self-authored impl is genuinely buggy.

NOTE: the self-authored impl is agent-generated code, exec'd here. The tasks are
tiny pure functions; still, this is arbitrary-code execution and is flagged.
"""

import json
import sys
from pathlib import Path

HERE = Path(__file__).resolve().parent
REPO = HERE.parents[1]
CASES = HERE / "cases"

sys.path.insert(0, str(HERE))
import tasks  # noqa: E402


def _eq(a, b):
    if isinstance(a, bool) or isinstance(b, bool):
        return a == b
    if isinstance(a, (int, float)) and isinstance(b, (int, float)):
        return abs(a - b) < 1e-9
    return a == b


def load_impl(source, name):
    ns = {}
    exec(source, ns)            # noqa: S102 -- agent code, tiny pure function (flagged)
    return ns[name]


def call(fn, inp):
    return fn(inp)


def main():
    result = {"per_task": {}}
    n_buggy = self_caught = indep_caught = 0
    for t in tasks.TASKS:
        name, oracle, probes = t["name"], t["correct"], t["probes"]
        sa = CASES / f"{name}.selfauthor.json"
        ind = CASES / f"{name}.independent.json"
        rec = {"impl_buggy": None}
        if not sa.exists():
            result["per_task"][name] = {"note": "no selfauthor file"}
            continue
        blob = json.loads(sa.read_text())
        try:
            f = load_impl(blob["impl"], name)
        except Exception as e:
            result["per_task"][name] = {"note": f"impl failed to load: {e}"}
            continue
        self_tests = blob.get("tests", [])
        indep_tests = json.loads(ind.read_text()) if ind.exists() else []

        # is the self-authored impl buggy? (probes + all test inputs)
        all_inputs = probes + [c["input"] for c in self_tests + indep_tests]
        buggy = False
        for inp in all_inputs:
            try:
                if not _eq(call(f, inp), oracle(inp)):
                    buggy = True
                    break
            except Exception:
                buggy = True
                break
        rec["impl_buggy"] = buggy

        def caught_by(testset):
            for c in testset:
                inp, exp = c.get("input"), c.get("expected")
                try:
                    o = oracle(inp)
                    fi = call(f, inp)
                except Exception:
                    continue
                if _eq(exp, o) and not _eq(o, fi):   # spec-correct test the impl fails
                    return True
            return False

        rec["self_caught"] = caught_by(self_tests)
        rec["indep_caught"] = caught_by(indep_tests)
        rec["n_self"] = len(self_tests)
        rec["n_indep"] = len(indep_tests)
        if buggy:
            n_buggy += 1
            self_caught += rec["self_caught"]
            indep_caught += rec["indep_caught"]
        result["per_task"][name] = rec

    result["summary"] = {
        "tasks": len(tasks.TASKS), "buggy_impls": n_buggy,
        "self_caught_of_buggy": self_caught, "indep_caught_of_buggy": indep_caught,
    }
    (HERE / "results.json").write_text(json.dumps(result, indent=2, sort_keys=True) + "\n")

    macro = {
        "ResPilotTwoTasks": len(tasks.TASKS),
        "ResPilotTwoBuggy": n_buggy,
        "ResPilotTwoSelfCaught": self_caught,
        "ResPilotTwoIndepCaught": indep_caught,
    }
    mac = REPO / "tex" / "macros" / "pilot2.tex"
    mac.parent.mkdir(parents=True, exist_ok=True)
    lines = ["% GENERATED by eval/pilot2/score.py -- do not hand-edit."]
    for k in sorted(macro):
        lines.append(f"\\newcommand{{\\{k}}}{{{macro[k]}}}")
    mac.write_text("\n".join(lines) + "\n")

    print(json.dumps(result, indent=2, sort_keys=True))
    print(f"\nbuggy self-authored impls: {n_buggy}/{len(tasks.TASKS)}; "
          f"of those, self-tests caught {self_caught}, independent caught {indep_caught}")
    print("macros ->", mac)
    return 0


if __name__ == "__main__":
    sys.exit(main())
