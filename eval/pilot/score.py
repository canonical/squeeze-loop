#!/usr/bin/env python3
"""Score the pilot: did each config's exerciser catch the seeded defect?

Reads the exerciser-written test cases in eval/pilot/cases/<task>.<config>.json
(each a JSON array of {"input": <arg>, "expected": <value>}) and judges, against
the reference implementations in tasks.py:

  a config CATCHES a task iff some test case is (a) discriminating --
  correct(input) != wrong(input) -- and (b) spec-correct -- expected ==
  correct(input). That is exactly a test the seeded-wrong implementation fails.

Also reports spec-validity (fraction of an exerciser's cases whose expected
matches the spec) as a sanity check that a "miss" is anchoring, not incompetence.
"""

import json
import sys
from pathlib import Path

HERE = Path(__file__).resolve().parent
REPO = HERE.parents[1]
CASES = HERE / "cases"
CONFIGS = ["barrier_on", "barrier_off"]

sys.path.insert(0, str(HERE))
import tasks  # noqa: E402


def _eq(a, b):
    if isinstance(a, bool) or isinstance(b, bool):
        return a == b
    if isinstance(a, (int, float)) and isinstance(b, (int, float)):
        return abs(a - b) < 1e-9
    return a == b


def main():
    by_task = {t["name"]: t for t in tasks.TASKS}
    result = {"configs": {}, "per_task": {}}
    for cfg in CONFIGS:
        caught = 0
        valid_cases = total_cases = 0
        details = {}
        for name, t in by_task.items():
            f = CASES / f"{name}.{cfg}.json"
            if not f.exists():
                details[name] = {"caught": False, "note": "no cases file"}
                continue
            cases = json.loads(f.read_text())
            task_caught = False
            v = 0
            for c in cases:
                inp, exp = c["input"], c["expected"]
                try:
                    cor, wr = t["correct"](inp), t["wrong"](inp)
                except Exception:
                    continue
                if _eq(exp, cor):
                    v += 1
                if (not _eq(cor, wr)) and _eq(exp, cor):
                    task_caught = True
            caught += task_caught
            valid_cases += v
            total_cases += len(cases)
            details[name] = {"caught": task_caught, "n_cases": len(cases),
                             "spec_correct": v}
            result["per_task"].setdefault(name, {})[cfg] = task_caught
        result["configs"][cfg] = {
            "tasks_caught": caught,
            "tasks_total": len(by_task),
            "spec_validity_pct": round(100 * valid_cases / total_cases) if total_cases else 0,
            "details": details,
        }

    (HERE / "results.json").write_text(json.dumps(result, indent=2, sort_keys=True) + "\n")

    n = len(by_task)
    on = result["configs"]["barrier_on"]["tasks_caught"]
    off = result["configs"]["barrier_off"]["tasks_caught"]
    macro = {
        "ResPilotTasks": n,
        "ResPilotDetectionOn": on,
        "ResPilotDetectionOff": off,
        "ResPilotValidityOn": result["configs"]["barrier_on"]["spec_validity_pct"],
        "ResPilotValidityOff": result["configs"]["barrier_off"]["spec_validity_pct"],
    }
    macros = REPO / "tex" / "macros" / "pilot.tex"
    macros.parent.mkdir(parents=True, exist_ok=True)
    lines = ["% GENERATED by eval/pilot/score.py -- do not hand-edit."]
    for k in sorted(macro):
        lines.append(f"\\newcommand{{\\{k}}}{{{macro[k]}}}")
    macros.write_text("\n".join(lines) + "\n")

    print(json.dumps(result, indent=2, sort_keys=True))
    print(f"\nbarrier_on caught {on}/{n}; barrier_off caught {off}/{n}")
    print("macros ->", macros)
    return 0


if __name__ == "__main__":
    sys.exit(main())
